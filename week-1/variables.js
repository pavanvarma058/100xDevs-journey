var a = 1; // var is a variable to define the value and it is used when ever we don't want to change the value
a = 2;
console.log(a); // 2

let b = 1; // let is also variable to define the value and it is used when ever we want to change the value
b = 3;
console.log(b); // 3

const c = 2; // it means constant, we cannot change the value once it is defined as const, if we try to change it throws error
console.log(c);
